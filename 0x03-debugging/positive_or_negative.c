#include <stdio.h>
#include <stdlib.h>

/**
 * positive_or_negative - check sign
 * @i: number to check
 * description: fkrfkfkf
 */
void positive_or_negative(int i)
{
if (i > 0)
{
printf("%d is positive\n", i);
}
else if (i == 0)
{
printf("%d is zero\n", i);
}
else
printf("%d is negative\n", i);
}
